                <h1><span class="highlight"><strong>Glossário</strong></span> Histórico</h1>
        <ul>
            <li><strong>Absolutismo:</strong> Sistema de governo em que o monarca detinha poder absoluto.</li>
            <li><strong>Aculturação:</strong> Processo de troca de elementos culturais entre diferentes grupos.</li>
            <li><strong>Agricultura:</strong> Técnica de cultivo de plantas e criação de animais.</li>
            <li><strong>Alienação:</strong> Processo em que o indivíduo se sente desconectado da sociedade ou do trabalho.</li>
            <li><strong>América:</strong> Continente descoberto por Cristóvão Colombo em 1492.</li>
            <li><strong>Antigo Egito:</strong> Civilização antiga localizada no nordeste da África, às margens do Rio Nilo.</li>
            <li><strong>Arte Rupestre:</strong> Pinturas e gravuras feitas nas rochas, principalmente na pré-história.</li>
            <li><strong>Associação:</strong> Ato de se agrupar com outros com o objetivo de atingir fins comuns.</li>
            <li><strong>Atividade Industrial:</strong> Produção de bens em grande escala utilizando máquinas e recursos naturais.</li>
            <li><strong>Autoritarismo:</strong> Forma de governo em que o poder é centralizado nas mãos de uma única pessoa.</li>
            <li><strong>Capitulacões:</strong> Tratados que garantiam direitos especiais para estrangeiros em territórios dominados.</li>
            <li><strong>Capitalismo:</strong> Sistema econômico baseado na produção para o mercado e propriedade privada.</li>
            <li><strong>Cidades-Estado:</strong> Cidades autônomas com governo próprio, como na Grécia Antiga.</li>
            <li><strong>Cidadania:</strong> Direitos e deveres que uma pessoa tem dentro de uma nação.</li>
            <li><strong>Colonialismo:</strong> Processo de exploração e dominação de outros territórios para benefícios econômicos.</li>
            <li><strong>Comunismo:</strong> Sistema político e econômico que visa a igualdade social e a eliminação da propriedade privada.</li>
            <li><strong>Conquista:</strong> Processo de tomada de territórios por meio de guerras e expedições.</li>
            <li><strong>Constituição:</strong> Conjunto de leis que estabelece os direitos e deveres de um país.</li>
            <li><strong>Democracia:</strong> Sistema político em que o poder é exercido pelo povo, por meio de eleições.</li>
            <li><strong>Despotismo:</strong> Governo absoluto de um monarca ou líder.</li>
            <li><strong>Difusão Cultural:</strong> Espalhamento de elementos culturais entre diferentes grupos sociais.</li>
            <li><strong>Ditadura:</strong> Governo em que o poder é concentrado nas mãos de uma única pessoa ou grupo.</li>
            <li><strong>Divisão do Trabalho:</strong> Organização de tarefas em uma sociedade, com especialização em diferentes funções.</li>
            <li><strong>Doutrina Monroe:</strong> Política externa dos Estados Unidos, que visava impedir a intervenção europeia nas Américas.</li>
            <li><strong>Empirismo:</strong> Teoria filosófica que defende o conhecimento como resultado da experiência.</li>
            <li><strong>Enlightenment:</strong> Movimento intelectual do século XVIII que enfatizava razão, ciência e direitos humanos.</li>
            <li><strong>Escambo:</strong> Troca direta de bens sem o uso de dinheiro.</li>
            <li><strong>Escravidão:</strong> Sistema de trabalho forçado, onde os indivíduos são considerados propriedade de outro.</li>
            <li><strong>Estado-nação:</strong> Forma de organização política em que um território corresponde a um Estado independente.</li>
            <li><strong>Feudalismo:</strong> Sistema social e econômico medieval baseado nas relações de vassalagem e posse de terras.</li>
            <li><strong>Filosofia:</strong> Estudo das questões fundamentais sobre existência, conhecimento e ética.</li>
            <li><strong>Fronteira:</strong> Linha que demarca os limites de um território ou estado.</li>
            <li><strong>Globalização:</strong> Processo de integração econômica, política e cultural em uma escala mundial.</li>
            <li><strong>Guerra Fria:</strong> Período de tensão política e militar entre os Estados Unidos e a União Soviética após a Segunda Guerra Mundial.</li>
            <li><strong>Guerra Fria:</strong> Conflito ideológico entre blocos capitalista e comunista no século XX.</li>
            <li><strong>Guerra dos Trinta Anos:</strong> Conflito religioso e político entre países europeus, que durou de 1618 a 1648.</li>
            <li><strong>Humanismo:</strong> Movimento cultural do Renascimento que enfatizava o valor do ser humano e suas capacidades.</li>
            <li><strong>Iluminismo:</strong> Movimento intelectual do século XVIII que defendia a razão, a ciência e os direitos humanos.</li>
            <li><strong>Imigração:</strong> Movimento de pessoas que se mudam de um país para outro com a intenção de viver permanentemente.</li>
            <li><strong>Imperialismo:</strong> Política de expansão territorial de um Estado sobre outros.</li>
            <li><strong>Indústria:</strong> Conjunto de atividades econômicas voltadas para a produção de bens e serviços em larga escala.</li>
            <li><strong>Independência:</strong> Ato de um país tornar-se autônomo e livre de qualquer forma de dominação externa.</li>
            <li><strong>Inovação:</strong> Criação de novos produtos ou processos que melhoram ou transformam as atividades humanas.</li>
            <li><strong>Instituição:</strong> Estrutura organizacional que visa estabelecer regras e condutas dentro de uma sociedade.</li>
            <li><strong>Judaismo:</strong> Religião monoteísta baseada nos ensinamentos da Torá.</li>
            <li><strong>Liberalismo:</strong> Ideologia política que defende a liberdade individual e a economia de mercado.</li>
            <li><strong>Macroeconomia:</strong> Estudo da economia em larga escala, incluindo políticas fiscais e monetárias.</li>
            <li><strong>Mercantilismo:</strong> Sistema econômico do período moderno baseado na acumulação de metais preciosos e controle do comércio.</li>
            <li><strong>Mesoamérica:</strong> Região da América Central que abrange várias civilizações antigas, como os maias e astecas.</li>
            <li><strong>Monarquia:</strong> Governo em que o poder é exercido por um monarca ou rei.</li>
            <li><strong>Movimento Operário:</strong> Conjunto de ações que visam melhorar as condições de trabalho e de vida dos operários.</li>
            <li><strong>Monoteísmo:</strong> Crença em um único Deus, característica de religiões como o Cristianismo, Islamismo e Judaísmo.</li>
            <li><strong>Neocolonialismo:</strong> Forma de dominação econômica e política de países independentes, principalmente na África.</li>
            <li><strong>Oligarquia:</strong> Governo onde o poder é concentrado nas mãos de um pequeno número de pessoas ou grupos.</li>
            <li><strong>Operarismo:</strong> Doutrina marxista que foca nas condições de trabalho e na revolução operária.</li>
            <li><strong>Panamericanismo:</strong> Movimento que visa a integração dos países das Américas.</li>
            <li><strong>Parlamento:</strong> Órgão legislativo onde são discutidas e aprovadas as leis de um país.</li>
            <li><strong>Proletariado:</strong> Classe trabalhadora que depende da venda da sua força de trabalho para viver.</li>
            <li><strong>Renascentismo:</strong> Movimento cultural e artístico que ocorreu no século XV, com foco no humanismo e nas artes.</li>
            <li><strong>Revolução Francesa:</strong>Revolta social na França (1789-1799) que derrubou a monarquia e fortaleceu ideais republicanos.</li>
            <li><strong>Revolução Industrial:</strong> Transformações econômicas e tecnológicas do século XIX que mudaram a produção e o trabalho.</li>
        </ul>
