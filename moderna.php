        <section class="section iluminismo">
            <h3>Iluminismo</h3>
            <div class="content">
                <div class="text">
                    <p>O Iluminismo foi um movimento intelectual do século XVIII que defendia a razão, a ciência e os direitos individuais como base para a organização da sociedade. Filósofos como Voltaire, Montesquieu, e Rousseau criticaram o absolutismo e a Igreja, propondo novas ideias sobre liberdade, igualdade e justiça. Eles acreditavam que a educação e o conhecimento poderiam transformar a sociedade para melhor.</p>
                    <p>As ideias iluministas influenciaram as revoluções que marcaram o final da Idade Moderna, como a Revolução Americana e a Revolução Francesa, que buscavam implantar governos democráticos e o fim dos privilégios de classe.</p>
                </div>
